#include <stdio.h>
/**
 * add - Adds two ints
 * @a: First int parameter
 * @b: Second int parameter
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * add - Subtracts two ints
 * @a: First int parameter
 * @b: Second int parameter
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * add - Multiplies two ints
 * @a: First int parameter
 * @b: Second int parameter
 * Return: product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * add - Divides two ints
 * @a: First int parameter
 * @b: Second int parameter
 * Return: quotient a and b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * add - Finds modulus of a in b
 * @a: First int parameter
 * @b: Second int parameter
 * Return: a mod b
 */
int mod(int a, int b)
{
	return (a % b);
}
